#condicionais
valor = 2
if valor == 10:
    print(f"A variavel valor, que vale {valor}, é igual a 10!")#NÂO SERÁ EXECUTADO
if valor != 10:
        print(f"A variavel valor, que vale {valor}, é diferente de 10!")#ESSE SERÁ EXECUTADO
if valor > 10:
        print(f"A variavel valor, que vale {valor}, é maior a 10!")#ESSE NÃO SERÁ EXECUTADO
if valor >= 10:
        print(f"A variavel valor, que vale {valor}, é maior ou igual a 10!")#ESSE NÃO SERÁ EXECUTADO
if valor < 10:
        print(f"A variavel valor, que vale {valor}, é menor que 10!")#ESSE SERÁ EXECUTADO
if valor <= 10:
        print(f"A variavel valor, que vale {valor}, é menor ou igual que 10!")#ESSE SERÁ EXECUTADO